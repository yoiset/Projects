package es.dgoj.rgiaj.business.beans;

import com.dgoj.core.common.entity.AbstractQueryEntity;

/**
 * Criterios de busqueda de cartas.
 * @author dgonzalez
 */
public class CartaQueryBean extends AbstractQueryEntity {

	/**
	 * serialVersionUID.
	 */
	private static final long serialVersionUID = 1L;
	
	/** Campo id. */
	private Long id;

	/** Campo descripcion. */
	private String descripcion;
	
	/** Campo cargo (refencia al id de su tabla. */
	private Long cargo;
	
	/** Campo responsable (refencia al id de su tabla. */
	private Long responsable;
	
	/**
	 * Devuelve el valor del campo id.
	 *
	 * @return Id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * Establece el valor del campo id.
	 *
	 * @param id
	 */
	public void setId(Long id) {
		this.id = id;
	}

	
	/**
	 * Devuelve el valor del campo descripcion.
	 * 
	 * @return descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 *  Establece el valor del campo descripcion.
	 *  
	 * @param descripcion
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	/**
	 * Devuelve el valor del campo cargo.
	 * 
	 * @return cargo
	 */
	public Long getCargo() {
		return cargo;
	}

	/**
	 * Establece el valor del campo cargo.
	 * 
	 * @param cargo
	 */
	public void setCargo(Long cargo) {
		this.cargo = cargo;
	}

	/**
	 * Devuelve el valor del campo responsable.
	 * 
	 * @return responsable
	 */
	public Long getResponsable() {
		return responsable;
	}

	/**
	 * Establece el valor del campo responable.
	 * 
	 * @param responsable
	 */
	public void setResponsable(Long responsable) {
		this.responsable = responsable;
	}


	
}
