package es.dgoj.rgiaj.form;

import org.springframework.beans.factory.annotation.Configurable;

/**
 * Formulario de busqueda de cartas.
 *
 * @author connectis
 */
@Configurable
public class SearchCartaForm {

	/** Campo id. */
	private String id;

	/** Campo descripcion. */
	private String descripcion;
	
	/** Campo cargo (refencia al id de su tabla). */
	private String cargo;
	
	/** Campo responsable (refencia al id de su tabla). */
	private String responsable;
	
	/**
	 * Devuelve el valor del campo id.
	 *
	 * @return Id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * Establece el valor del campo id.
	 *
	 * @param id the id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Devuelve el valor del campo descripcion.
	 *
	 * @return Descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}

	/**
	 * Establece el valor del campo descripcion.
	 *
	 * @param descripcion
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	/**
	 * Devuelve el valor del campo cargo.
	 * 
	 * @return cargo
	 */
	public String getCargo() {
		return cargo;
	}

	/**
	 * Establece el valor del campo cargo.
	 * 
	 * @param cargo
	 */
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}

	/**
	 * Devuelve el valor del campo responsable.
	 * 
	 * @return responsable
	 */
	public String getResponsable() {
		return responsable;
	}

	/**
	 * Establece el valor del campo responable.
	 * 
	 * @param responsable
	 */
	public void setResponsable(String responsable) {
		this.responsable = responsable;
	}
	
}
